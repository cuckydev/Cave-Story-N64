static u32 selected_profile;
static s32 sy;
static u8 col;
static u8 black_time;

void ModeDebugSel_Init()
{
	selected_profile = 0;
	black_time = 0;
	sy = -320;
	col = 0;
}

GameMode ModeDebugSel_Proc()
{
	if (!(black_time & 0x80))
	{
		//Scroll when up / down is pressed
		if ((gKeyTrg & gKeyUp) && selected_profile > 0)
			selected_profile--;
		if ((gKeyTrg & gKeyDown) && profiles[selected_profile + 1].profile_data != NULL)
			selected_profile++;
		
		//Select once A is pressed
		if ((gKeyTrg & gKeyOk) && col >= 0x20)
			black_time = 30 | 0x80;
		
		//Scroll background
		sy += ((s32)((selected_profile * 24) - 120) - sy) / 8;
		
		//Fade in background
		if (col < 0x40)
			col += 4;
	}
	else
	{
		//Fade and scroll
		sy += (-320 - sy) / 8;
		if (col > 0x00)
			col -= 4;
		if (!(--black_time & 0x80))
		{
			//Load selected profile
			LoadFromProfile(profiles[selected_profile].profile_data);
			return GameMode_Action;
		}
	}
	
	return GameMode_DebugSelect;
}

void ModeDebugSel_Draw()
{
	u32 i;
	s32 y;
	
	//Draw background
	CortBox(&grcFull, RGB(0x00, col, col));
	
	//Draw all selections
	for (i = 0; profiles[i].profile_data; i++)
	{
		y = (i * 24) - sy - 6;
		if (y < -6 || y > (SCREEN_HEIGHT + 6))
			continue;
		if (i == selected_profile)
		{
			PutText(32+1, y+1, profiles[i].name, RGB(0x00, 0x00, 0x00));
			PutText(32+0, y+0, profiles[i].name, RGB(0x00, 0x00, 0x00));
			PutText(32-1, y-1, profiles[i].name, RGB(0xFF, 0xFF, 0xFF));
		}
		else
		{
			PutText(32+1, y+1, profiles[i].name, RGB(0x00, 0x00, 0x00));
			PutText(32+0, y+0, profiles[i].name, RGB(0x80, 0x80, 0x80));
		}
	}
}

